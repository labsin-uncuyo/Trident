#!/usr/bin/env python3
"""
Count tokens used in an opencode session from a *_timeline.jsonl log file.

Usage:
    python3 scripts/count_tokens.py <path_to_timeline.jsonl>

Token accounting (per step_finish event):
  - input   : tokens sent to the model (grows with context each step)
  - output  : new tokens generated by the model in that step
  - total   : input + output for that API call
  - cache   : read/write tokens served from/to the prompt cache

Reported metrics:
  - Total API calls (step_finish events)
  - Sum of all 'total' tokens billed across every API call
  - Sum of output tokens (net new tokens generated)
  - Peak context size (max 'total' in a single call, approximate window usage)
  - Cache read / write totals
  - Per-session breakdown when multiple sessions exist
"""

import json
import sys
from collections import defaultdict


def parse_tokens(path: str):
    sessions = defaultdict(lambda: {
        "steps": 0,
        "total": 0,
        "input": 0,
        "output": 0,
        "reasoning": 0,
        "cache_read": 0,
        "cache_write": 0,
        "peak": 0,
    })

    with open(path, encoding="utf-8") as f:
        for lineno, line in enumerate(f, 1):
            line = line.strip()
            if not line:
                continue
            try:
                event = json.loads(line)
            except json.JSONDecodeError as e:
                print(f"  [warn] line {lineno}: JSON parse error – {e}", file=sys.stderr)
                continue

            if event.get("msg") != "step_finish":
                continue

            part = event.get("data", {}).get("part", {})
            tokens = part.get("tokens")
            if not tokens:
                continue

            session_id = part.get("sessionID", "unknown")
            s = sessions[session_id]

            t_total    = tokens.get("total", 0)
            t_input    = tokens.get("input", 0)
            t_output   = tokens.get("output", 0)
            t_reasoning = tokens.get("reasoning", 0)
            t_cache    = tokens.get("cache", {})
            t_cr       = t_cache.get("read", 0)
            t_cw       = t_cache.get("write", 0)

            s["steps"]       += 1
            s["total"]       += t_total
            s["input"]       += t_input
            s["output"]      += t_output
            s["reasoning"]   += t_reasoning
            s["cache_read"]  += t_cr
            s["cache_write"] += t_cw
            if t_total > s["peak"]:
                s["peak"] = t_total

    return sessions


def fmt(n: int) -> str:
    return f"{n:,}"


def print_session(sid: str, s: dict):
    print(f"  Session : {sid}")
    print(f"    API calls (steps)    : {fmt(s['steps'])}")
    print(f"    Total tokens billed  : {fmt(s['total'])}")
    print(f"      ├─ input           : {fmt(s['input'])}")
    print(f"      ├─ output          : {fmt(s['output'])}")
    if s["reasoning"]:
        print(f"      ├─ reasoning       : {fmt(s['reasoning'])}")
    if s["cache_read"] or s["cache_write"]:
        print(f"      ├─ cache read      : {fmt(s['cache_read'])}")
        print(f"      └─ cache write     : {fmt(s['cache_write'])}")
    print(f"    Peak context / call  : {fmt(s['peak'])}")


def main():
    if len(sys.argv) < 2:
        print("Usage: python3 count_tokens.py <path_to_timeline.jsonl>", file=sys.stderr)
        sys.exit(1)

    path = sys.argv[1]

    try:
        sessions = parse_tokens(path)
    except FileNotFoundError:
        print(f"Error: file not found: {path}", file=sys.stderr)
        sys.exit(1)

    if not sessions:
        print("No step_finish events with token data found in the file.")
        sys.exit(0)

    # Aggregate totals across all sessions
    grand = {k: 0 for k in ("steps", "total", "input", "output", "reasoning", "cache_read", "cache_write")}
    grand_peak = 0
    for s in sessions.values():
        for k in grand:
            grand[k] += s[k]
        if s["peak"] > grand_peak:
            grand_peak = s["peak"]

    print(f"\n{'='*60}")
    print(f"  Token report for: {path}")
    print(f"{'='*60}")
    print(f"\n  Sessions found     : {len(sessions)}")

    if len(sessions) > 1:
        print()
        for sid, s in sessions.items():
            print_session(sid, s)
            print()
        print(f"{'─'*60}")
        print(f"  GRAND TOTAL across all sessions")
        print(f"    API calls (steps)    : {fmt(grand['steps'])}")
        print(f"    Total tokens billed  : {fmt(grand['total'])}")
        print(f"      ├─ input           : {fmt(grand['input'])}")
        print(f"      ├─ output          : {fmt(grand['output'])}")
        if grand["reasoning"]:
            print(f"      ├─ reasoning       : {fmt(grand['reasoning'])}")
        if grand["cache_read"] or grand["cache_write"]:
            print(f"      ├─ cache read      : {fmt(grand['cache_read'])}")
            print(f"      └─ cache write     : {fmt(grand['cache_write'])}")
        print(f"    Peak context / call  : {fmt(grand_peak)}")
    else:
        sid, s = next(iter(sessions.items()))
        print()
        print_session(sid, s)

    print(f"\n{'='*60}\n")


if __name__ == "__main__":
    main()
