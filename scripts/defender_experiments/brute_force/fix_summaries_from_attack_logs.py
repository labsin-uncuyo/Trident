#!/usr/bin/env python3
"""
Fix Flask brute force experiment summaries using the actual attack data.

This script corrects the following fields in flask_brute_experiment_summary.json:
- flask_login_attempts: Total number of login attempts made
- flask_successful_attempts: Number of successful login attempts (0 or 1)
- password_found: Boolean indicating if password was found

Data sources (in order of preference):
1. logs/flask_attack_summary.json - Authoritative source from attack monitoring
2. logs/flask_attack_log.txt - Parsed for attempt numbers (has duplicate lines)
"""

import argparse
import json
import re
import os
from pathlib import Path
from typing import Dict, Tuple, Optional


def parse_attack_summary(summary_path: str) -> Tuple[int, int, bool, Optional[str]]:
    """
    Parse the Flask attack summary JSON to extract attempt statistics.

    This is the authoritative source generated by the attack monitoring script.

    Args:
        summary_path: Path to the flask_attack_summary.json file

    Returns:
        Tuple of (total_attempts, successful_attempts, password_found, success_time)
    """
    if not os.path.exists(summary_path):
        raise FileNotFoundError(f"Attack summary not found: {summary_path}")

    # Read and fix common JSON errors (like unquoted values)
    with open(summary_path, 'r') as f:
        content = f.read()

    # Fix common JSON syntax errors
    # e.g., "success": interrupted -> "success": "interrupted"
    content = re.sub(r'"success":\s*([a-zA-Z_][a-zA-Z0-9_]*)', r'"success": "\1"', content)

    data = json.loads(content)

    # Extract data from the attack summary
    total_attempts = data.get('total_attempts_before_blocked', data.get('current_attempt', 0))
    password_found = data.get('guess_password_successfully', False) == True

    # The attack summary doesn't have a success time field, so we return None
    successful_attempts = 1 if password_found else 0
    success_time = None

    return total_attempts, successful_attempts, password_found, success_time


def parse_attack_log(log_path: str) -> Tuple[int, int, bool, Optional[str]]:
    """
    Parse the Flask attack log to extract attempt statistics.

    This is a fallback method when flask_attack_summary.json doesn't exist.
    Note: This log has duplicate lines, so we track the last attempt number seen.

    Args:
        log_path: Path to the flask_attack_log.txt file

    Returns:
        Tuple of (total_attempts, successful_attempts, password_found, success_time)
    """
    if not os.path.exists(log_path):
        raise FileNotFoundError(f"Attack log not found: {log_path}")

    total_attempts = 0
    successful_attempts = 0
    password_found = False
    success_time = None

    # Patterns to match
    attempt_pattern = re.compile(r'\[.*?\] Attempt (\d+)/\d+: Trying password')

    # Success pattern - matches multiple formats:
    # - [2026-02-24 00:58:00] SUCCESS: Password 'admin' found for admin@... (attempt 3000)
    # - [2026-02-24 00:58:00] SUCCESS: Password found! 'admin' is the correct password
    # Note: Must be at start of line after timestamp to avoid matching "Phase 3 completed"
    success_pattern = re.compile(
        r'^\[(\d{4}-\d{2}-\d{2} \d{2}:\d{2}:\d{2})\] SUCCESS: Password'
    )

    success_pattern_with_attempt = re.compile(
        r'\[.*?\] SUCCESS: Password \'(\w+)\' found for \w+@.*? \(attempt (\d+)\)'
    )

    with open(log_path, 'r') as f:
        for line in f:
            # Check for successful attempt with attempt number (older format)
            success_attempt_match = success_pattern_with_attempt.search(line)
            if success_attempt_match:
                attempt_num = int(success_attempt_match.group(2))
                successful_attempts = 1  # At most 1 successful password find per attack
                password_found = True
                total_attempts = attempt_num
                continue

            # Check for successful attempt (newer format without attempt number)
            success_match = success_pattern.search(line)
            if success_match:
                success_time = success_match.group(1)
                successful_attempts = 1  # At most 1 successful password find per attack
                password_found = True
                # Don't update total_attempts here - it will be the last attempt seen
                continue

            # Track the last attempt number seen
            attempt_match = attempt_pattern.search(line)
            if attempt_match:
                total_attempts = int(attempt_match.group(1))

    return total_attempts, successful_attempts, password_found, success_time


def fix_summary(summary_path: str, run_dir: str, dry_run: bool = False) -> Dict:
    """
    Fix the experiment summary file using data from attack logs.

    Args:
        summary_path: Path to the flask_brute_experiment_summary.json file
        run_dir: Path to the run directory (containing logs/)
        dry_run: If True, print changes without modifying files

    Returns:
        Dictionary with old and new values for comparison
    """
    # Load the summary
    with open(summary_path, 'r') as f:
        summary = json.load(f)

    # Try to get data from flask_attack_summary.json first (authoritative source)
    attack_summary_path = os.path.join(run_dir, 'logs', 'flask_attack_summary.json')
    attack_log_path = os.path.join(run_dir, 'logs', 'flask_attack_log.txt')

    data_source = None
    try:
        if os.path.exists(attack_summary_path):
            total_attempts, successful_attempts, password_found, success_time = \
                parse_attack_summary(attack_summary_path)
            data_source = "flask_attack_summary.json"
        elif os.path.exists(attack_log_path):
            total_attempts, successful_attempts, password_found, success_time = \
                parse_attack_log(attack_log_path)
            data_source = "flask_attack_log.txt"
        else:
            raise FileNotFoundError("No attack data files found")
    except Exception as e:
        raise Exception(f"Failed to parse attack data: {e}")

    # Get old values
    old_values = {
        'flask_login_attempts': summary['metrics'].get('flask_login_attempts'),
        'flask_successful_attempts': summary['metrics'].get('flask_successful_attempts'),
        'password_found': summary['metrics'].get('password_found'),
        'flask_successful_attempt_time': summary['metrics'].get('flask_successful_attempt_time'),
    }

    # Update metrics
    summary['metrics']['flask_login_attempts'] = total_attempts
    summary['metrics']['flask_successful_attempts'] = successful_attempts
    summary['metrics']['password_found'] = password_found

    # Update successful attempt time if found
    if password_found and success_time:
        # Convert log time format to match summary format
        # Log format: 2026-02-24 00:58:00
        # Summary format: 2026-02-24T00:58:00.346215
        if '.' not in success_time:
            success_time = success_time.replace(' ', 'T') + '.000000'
        else:
            success_time = success_time.replace(' ', 'T')
        summary['metrics']['flask_successful_attempt_time'] = success_time
    else:
        summary['metrics']['flask_successful_attempt_time'] = None

    new_values = {
        'flask_login_attempts': total_attempts,
        'flask_successful_attempts': successful_attempts,
        'password_found': password_found,
        'flask_successful_attempt_time': summary['metrics'].get('flask_successful_attempt_time'),
    }

    # Write back if not dry run
    if not dry_run:
        with open(summary_path, 'w') as f:
            json.dump(summary, f, indent=4)
        # Add trailing newline
        with open(summary_path, 'a') as f:
            f.write('\n')

    return {
        'old': old_values,
        'new': new_values,
        'changed': old_values != new_values,
        'data_source': data_source
    }


def process_directory(experiments_dir: str, dry_run: bool = False,
                      verbose: bool = False) -> Dict:
    """
    Process all experiment directories in the given path.

    Args:
        experiments_dir: Path to directory containing experiment run folders
                        (or a single experiment run directory)
        dry_run: If True, print changes without modifying files
        verbose: If True, print detailed information for each run

    Returns:
        Dictionary with summary statistics
    """
    results = {
        'processed': 0,
        'modified': 0,
        'errors': 0,
        'skipped': 0,
        'details': []
    }

    exp_path = Path(experiments_dir)
    if not exp_path.exists():
        raise FileNotFoundError(f"Directory not found: {experiments_dir}")

    # Check if this is a single run directory or a parent directory
    summary_path = exp_path / 'flask_brute_experiment_summary.json'

    if summary_path.exists():
        # This is a single run directory
        run_dirs = [exp_path]
    else:
        # This is a parent directory, find all experiment directories
        run_dirs = sorted(exp_path.glob('flask_brute_*_run_*'))

    for run_dir in run_dirs:
        summary_path = run_dir / 'flask_brute_experiment_summary.json'

        if not summary_path.exists():
            if verbose:
                print(f"SKIP: {run_dir.name} - No summary file found")
            results['skipped'] += 1
            continue

        try:
            result = fix_summary(str(summary_path), str(run_dir), dry_run=dry_run)
            results['processed'] += 1

            if result['changed']:
                results['modified'] += 1
                status = "MODIFIED"
            else:
                status = "unchanged"

            results['details'].append({
                'run': run_dir.name,
                'status': status,
                'changes': result
            })

            if verbose or result['changed']:
                source_info = f" ({result['data_source']})" if result.get('data_source') else ""
                print(f"{status}{source_info}: {run_dir.name}")
                if verbose and result['changed']:
                    print(f"  Old: attempts={result['old']['flask_login_attempts']}, "
                          f"successful={result['old']['flask_successful_attempts']}, "
                          f"found={result['old']['password_found']}")
                    print(f"  New: attempts={result['new']['flask_login_attempts']}, "
                          f"successful={result['new']['flask_successful_attempts']}, "
                          f"found={result['new']['password_found']}")

        except Exception as e:
            results['errors'] += 1
            print(f"ERROR: {run_dir.name} - {e}")

    return results


def main():
    parser = argparse.ArgumentParser(
        description='Fix Flask brute force experiment summaries using attack logs'
    )
    parser.add_argument(
        'experiments_dir',
        help='Path to directory containing experiment run folders'
    )
    parser.add_argument(
        '--dry-run',
        action='store_true',
        help='Print changes without modifying files'
    )
    parser.add_argument(
        '-v', '--verbose',
        action='store_true',
        help='Print detailed information for each run'
    )

    args = parser.parse_args()

    print(f"Processing experiments in: {args.experiments_dir}")
    if args.dry_run:
        print("DRY RUN MODE - No files will be modified\n")

    results = process_directory(
        args.experiments_dir,
        dry_run=args.dry_run,
        verbose=args.verbose
    )

    print(f"\n{'='*60}")
    print(f"Summary:")
    print(f"  Processed: {results['processed']}")
    print(f"  Modified:  {results['modified']}")
    print(f"  Skipped:   {results['skipped']}")
    print(f"  Errors:    {results['errors']}")

    if args.dry_run and results['modified'] > 0:
        print(f"\nRun without --dry-run to apply {results['modified']} changes")


if __name__ == '__main__':
    main()
